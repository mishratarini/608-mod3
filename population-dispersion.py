
import statistics

x = [1, 3, 4, 2, 6, 5, 3, 4, 5, 2]

print("Variance for the data in sec 4.18 is " + str(statistics.variance(x)))
print("Standard Deviation for the data in sec 4.18 is " + str(statistics.stdev(x)))
print("Population Variance for the data in sec 4.18 is " + str(statistics.pvariance(x)))
print("Population Standard Deviation for the data in sec 4.18 is " + str(statistics.pstdev(x)))


